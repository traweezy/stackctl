func TestDisableAutoGenTagsRecurses(t *testing.T) {
	root := &cobra.Command{Use: "root"}
	child := &cobra.Command{Use: "child"}
	grandchild := &cobra.Command{Use: "grandchild"}
	child.AddCommand(grandchild)
	root.AddCommand(child)

	disableAutoGenTags(root)

	for _, cmd := range []*cobra.Command{root, child, grandchild} {
		if !cmd.DisableAutoGenTag {
			t.Fatalf("expected %q to disable autogenerated tags", cmd.Name())
		}
	}
}

func TestRecreateDirRemovesExistingContents(t *testing.T) {
	root := openTempRoot(t)

	if err := root.MkdirAll(filepath.Join(markdownDir, "nested"), 0o750); err != nil {
		t.Fatalf("mkdir: %v", err)
	}
	if err := root.WriteFile(filepath.Join(markdownDir, "nested", "old.txt"), []byte("stale"), 0o600); err != nil {
		t.Fatalf("write stale file: %v", err)
	}

	if err := recreateDir(root, markdownDir); err != nil {
		t.Fatalf("recreate dir: %v", err)
	}

	entries, err := os.ReadDir(filepath.Join(root.Name(), markdownDir))
	if err != nil {
		t.Fatalf("read recreated dir: %v", err)
	}
	if len(entries) != 0 {
		t.Fatalf("expected recreated dir to be empty, got %d entries", len(entries))
	}
}

