func TestNormalizeManDatesClearsTHDate(t *testing.T) {
	root := openTempRoot(t)

	if err := root.MkdirAll(manDir, 0o750); err != nil {
		t.Fatalf("mkdir man dir: %v", err)
	}

	path := filepath.Join(manDir, "stackctl.1")
	content := ".TH \"stackctl\" \"1\" \"Apr 2026\" \"stackctl\" \"stackctl\"\n.SH NAME\nstackctl\n"
	if err := root.WriteFile(path, []byte(content), 0o600); err != nil {
		t.Fatalf("write man page: %v", err)
	}

	if err := normalizeManDates(root, manDir); err != nil {
		t.Fatalf("normalize man dates: %v", err)
	}

	normalized, err := root.ReadFile(path)
	if err != nil {
		t.Fatalf("read normalized man page: %v", err)
	}
	if !strings.Contains(string(normalized), ".TH \"stackctl\" \"1\" \"\" \"stackctl\" \"stackctl\"") {
		t.Fatalf("expected normalized .TH date field:\n%s", string(normalized))
	}
}

